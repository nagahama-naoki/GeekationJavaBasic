package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		
		int num1 = 1;
		double num2 = 2.2;
		String str1 = "私の好きな";
		String str2 = "です。";

		System.out.println(num1);
		System.out.println(num2);
		System.out.println(str1 + "整数は" + num1 + str2);
		System.out.println(str1 + "小数は" + num2 + str2);

	}

}
